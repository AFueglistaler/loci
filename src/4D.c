static const int NKS    = 256;

/* Declare private functions */
static void cellcoeffs(int ly, int lz, int lw, const real fs[], 
                       const real fs_x[], const real fs_y[], 
                       const real fs_z[], const real fs_w[], 
                       const real fs_xy[], const real fs_xz[], 
                       const real fs_xw[], const real fs_yz[], 
                       const real fs_yw[], const real fs_zw[], 
                       const real fs_xyz[], const real fs_xyw[], 
                       const real fs_xzw[], const real fs_yzw[], 
                       const real fs_xyzw[], real ks[NKS]);

static real poly(const real ks[NKS], int edx, int edy, int edz, int edw,
                 real x, real y, real z, real w);


/* "Constructor" */
Interpolation interpolation4D(const Range *rx, const Range *ry, 
                              const Range *rz, const Range *rw, 
                              const real fs[], const real fs_x[], 
                              const real fs_y[], const real fs_z[],
                              const real fs_w[], const real fs_xy[], 
                              const real fs_xz[], const real fs_xw[], 
                              const real fs_yz[], const real fs_yw[], 
                              const real fs_zw[], const real fs_xyz[], 
                              const real fs_xyw[], const real fs_xzw[], 
                              const real fs_yzw[], const real fs_xyzw[]) {

    int lx          = rx->len;
    int ly          = ry->len;
    int lz          = rz->len;
    int lw          = rw->len;
    int le          = lx*ly*lz*lw;

    real *ds_x      = malloc(le*sizeof(real));
    real *ds_y      = malloc(le*sizeof(real));
    real *ds_z      = malloc(le*sizeof(real));
    real *ds_w      = malloc(le*sizeof(real));
    real *ds_xy     = malloc(le*sizeof(real));
    real *ds_xz     = malloc(le*sizeof(real));
    real *ds_xw     = malloc(le*sizeof(real));
    real *ds_yz     = malloc(le*sizeof(real));
    real *ds_yw     = malloc(le*sizeof(real));
    real *ds_zw     = malloc(le*sizeof(real));
    real *ds_xyz    = malloc(le*sizeof(real));
    real *ds_xyw    = malloc(le*sizeof(real));
    real *ds_xzw    = malloc(le*sizeof(real));
    real *ds_yzw    = malloc(le*sizeof(real));
    real *ds_xyzw   = malloc(le*sizeof(real));

    for(int i = 0; i < lx; i++)  {
     for(int j = 0; j < ly; j++)  {
      for(int k = 0; k < lz; k++)  {
       for(int l = 0; l < lw; l++)  {
        int ijkl    = i*ly*lz*lw + j*lz*lw + k*lw + l;
        ds_x[ijkl]  = rx->dx*fs_x[ijkl];
        ds_y[ijkl]  = ry->dx*fs_y[ijkl];
        ds_z[ijkl]  = rz->dx*fs_z[ijkl];
        ds_w[ijkl]  = rw->dx*fs_w[ijkl];

        ds_xy[ijkl] = rx->dx*ry->dx*fs_xy[ijkl];
        ds_xz[ijkl] = rx->dx*rz->dx*fs_xz[ijkl];
        ds_xw[ijkl] = rx->dx*rw->dx*fs_xw[ijkl];
        ds_yz[ijkl] = ry->dx*rz->dx*fs_yz[ijkl];
        ds_yw[ijkl] = ry->dx*rw->dx*fs_yw[ijkl];
        ds_zw[ijkl] = rz->dx*rw->dx*fs_zw[ijkl];

        ds_xyz[ijkl]= rx->dx*ry->dx*rz->dx*fs_xyz[ijkl];
        ds_xyw[ijkl]= rx->dx*ry->dx*rw->dx*fs_xyw[ijkl];
        ds_xzw[ijkl]= rx->dx*rz->dx*rw->dx*fs_xzw[ijkl];
        ds_yzw[ijkl]= ry->dx*rz->dx*rw->dx*fs_yzw[ijkl];

        ds_xyzw[ijkl] = rx->dx*ry->dx*rz->dx*rw->dx*fs_xyzw[ijkl];
       }
      }
     }
    }

    Interpolation ip;
    ip.x0s  = malloc(4*sizeof(real)); 
    ip.dxs  = malloc(4*sizeof(real)); 
    ip.lens = malloc(4*sizeof(int)); 

    ip.x0s[0]   = rx->x0;
    ip.x0s[1]   = ry->x0;
    ip.x0s[2]   = rz->x0;
    ip.x0s[3]   = rw->x0;
    ip.dxs[0]   = rx->dx;
    ip.dxs[1]   = ry->dx;
    ip.dxs[2]   = rz->dx;
    ip.dxs[3]   = rw->dx;
    ip.lens[0]  = rx->len - 1;
    ip.lens[1]  = ry->len - 1;
    ip.lens[2]  = rz->len - 1;
    ip.lens[3]  = rw->len - 1;

    ip.ks  = malloc(ip.lens[0]*ip.lens[1]*ip.lens[2]*ip.lens[3]*
                    sizeof(real[NKS]));

    for(int i = 0; i < ip.lens[0]; i++)  {
     for(int j = 0; j < ip.lens[1]; j++)  {
      for(int k = 0; k < ip.lens[2]; k++)  {
       for(int l = 0; l < ip.lens[3]; l++)  {
        int ijklk   = NKS*(i*ip.lens[1]*ip.lens[2]*ip.lens[3] + 
                      j*ip.lens[2]*ip.lens[3] + k*ip.lens[3] + l);

        int ijklf   = i*ly*lz*lw + j*lz*lw + k*lw + l;

        cellcoeffs(ly, lz, lw, &fs[ijklf], &ds_x[ijklf], &ds_y[ijklf], 
                   &ds_z[ijklf], &ds_w[ijklf], &ds_xy[ijklf], &ds_xz[ijklf], 
                   &ds_xw[ijklf], &ds_yz[ijklf], &ds_yw[ijklf], &ds_zw[ijklf], 
                   &ds_xyz[ijklf], &ds_xyw[ijklf], &ds_xzw[ijklf], 
                   &ds_yzw[ijklf], &ds_xyzw[ijklf], &ip.ks[ijklk]);
       }
      }
     }
    }

    free(ds_x);
    free(ds_y);
    free(ds_z);
    free(ds_w);
    free(ds_xy);
    free(ds_xz);
    free(ds_xw);
    free(ds_yz);
    free(ds_yw);
    free(ds_zw);
    free(ds_xyz);
    free(ds_xyw);
    free(ds_xzw);
    free(ds_yzw);
    free(ds_xyzw);

    return ip;
}

real interpolate4D(const Interpolation* ip, real x, real y, real z, real w) {
    int i, j, k, l;
    real xp, yp, zp, wp;

    xp_i(&xp, &i, x, ip->x0s[0], ip->dxs[0]);
    xp_i(&yp, &j, y, ip->x0s[1], ip->dxs[1]);
    xp_i(&zp, &k, z, ip->x0s[2], ip->dxs[2]);
    xp_i(&wp, &l, w, ip->x0s[3], ip->dxs[3]);

    if(i>= 0 && i < ip->lens[0] && j>= 0 && j < ip->lens[1] && 
        k>= 0 && k < ip->lens[2] && l>= 0 && l < ip->lens[3])   {
        int ijkl  = NKS*(i*ip->lens[1]*ip->lens[2]*ip->lens[3] + 
                    j*ip->lens[2]*ip->lens[3] + k*ip->lens[3] + l);
        return poly(&ip->ks[ijkl], 0, 0, 0, 0, xp, yp, zp, wp);
    }
    else    {
        return NAN;
    }
}

